import { readFileSync, readdirSync } from 'fs';
const app = readFileSync('src/App.tsx','utf8');

// Parse lazy imports: const Foo = lazy(() => import("./pages/Foo"));
const imports = {};
for (const m of app.matchAll(/const\s+(\w+)\s*=\s*lazy\(\(\)\s*=>\s*import\("\.\/pages\/([^"]+)"\)\)/g)) {
  imports[m[1]] = m[2];
}
// Also direct imports
for (const m of app.matchAll(/import\s+(\w+)\s+from\s+"\.\/pages\/([^"]+)"/g)) {
  imports[m[1]] = m[2];
}

// Parse routes: <Route path="..." element={<Comp />} />
const routes = [];
for (const m of app.matchAll(/<Route\s+path="([^"]+)"\s+element=\{<(\w+)\s*\/?>\}/g)) {
  routes.push({ path: m[1], comp: m[2] });
}

// For each page file, find toursData["id"] usage
const pageFiles = readdirSync('src/pages');
const compToIds = {};
for (const f of pageFiles) {
  if (!f.endsWith('.tsx')) continue;
  const name = f.replace('.tsx','');
  const src = readFileSync(`src/pages/${f}`,'utf8');
  const ids = new Set();
  for (const m of src.matchAll(/toursData\[["']([^"']+)["']\]/g)) ids.add(m[1]);
  if (ids.size) compToIds[name] = [...ids];
}

// Build id -> route(s)
const idToRoutes = {};
for (const r of routes) {
  const page = imports[r.comp];
  if (!page) continue;
  const ids = compToIds[page];
  if (!ids) continue;
  for (const id of ids) {
    (idToRoutes[id] ||= []).push(r.path);
  }
}

// Load tour data ids
const td = readFileSync('src/data/tourData.ts','utf8');
const tourIds = [...td.matchAll(/^  "([a-z0-9-]+)": \{/gm)].map(m=>m[1]);

const missing = tourIds.filter(id => !idToRoutes[id]);
console.log('Total tours:', tourIds.length);
console.log('Missing routes:', missing.length);
console.log(missing.slice(0,50).join('\n'));
console.log('---sample mapped---');
for (const id of tourIds.slice(0,5)) console.log(id, '=>', idToRoutes[id]);

// Write map for use
import { writeFileSync } from 'fs';
const map = {};
for (const id of tourIds) {
  const rs = idToRoutes[id] || [];
  // prefer /thailand/... canonical (non-Navigate)
  const canonical = rs.find(p=>p.startsWith('/thailand/')) || rs[0];
  if (canonical) map[id] = canonical;
}
writeFileSync('src/data/tourRoutes.json', JSON.stringify(map, null, 2));
console.log('Wrote', Object.keys(map).length, 'entries');

const ts = '// AUTO-GENERATED by scripts/generate-tour-routes.mjs — do not edit by hand.\nexport const TOUR_ROUTES: Record<string, string> = ' + JSON.stringify(map, null, 2) + ';\n';
writeFileSync('src/data/tourRoutes.ts', ts);
console.log('Wrote src/data/tourRoutes.ts');
